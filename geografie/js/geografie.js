    $("#mapa path").on('mouseover', function () {
        if ($("#kraje:checked").val()) {
            puvodni = $(this).css('fill');
            $(this).css('fill', 'black');
            $(this).popover({
                'trigger': 'click',
                'placement': 'right',
                'offset': '10%, -100',
                'title': $(this).find('title').text()
            });
        }
        if (!$("#kraje").prop("checked")) {
            puvodni = $(this).css('fill');
            $(this).css('fill', puvodni);
        }
    });

    $("#mapa path").on('mouseout', function () {
        $(this).css('fill', puvodni);
    });

